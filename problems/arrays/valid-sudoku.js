function validateBox(board, i, j){
    const hashMap = {};
    for(let k = i; k <= i+2; k++){
        for(let l = j; l <= j+2; l++){
            if(board[k][l] !== '.'){
                if(hashMap[board[k][l]]){
                    return false;
                } else {
                    hashMap[board[k][l]] = true;
                }
            }
        }
    }
    return true
}
var isValidSudoku = function(board) {
    for(let row of board){
        const hashMap = {};
        for(let i = 0; i < row.length; i++){
            if(row[i] !== '.'){
                if(hashMap[row[i]]){
                    return false;
                } else {
                    hashMap[row[i]] = true;
                }
            }
        }
    }
    for(let i = 0; i < board[0].length; i++){
        const hashMap = {};
        for(let row of board){
            if(row[i] !== '.'){
                if(hashMap[row[i]]){
                    return false;
                } else {
                    hashMap[row[i]] = true;
                }
            }
        }
    }
    for(let i = 0; i < board[0].length; i += 3){
        for(let j = 0; j < board.length; j += 3){
            if(!validateBox(board, i, j)) return false;
        }
    }
    return true
};

